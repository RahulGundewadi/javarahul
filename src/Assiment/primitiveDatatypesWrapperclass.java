package Assiment;

public class primitiveDatatypesWrapperclass {

	public static void main(String[] args)
	{
		int i = 484;
		Integer inta = i;
		System.out.println(inta);
		
		byte b = 42;
		Byte bytea = b;
		System.out.println(bytea);
		
		char c = 'D';
		Character chara = c;
		System.out.println(chara);
		
		long l = 659566026l;
		Long longa = l;
		System.out.println(longa);
		
		float f = 49;
		Float floata = f;
		System.out.println(floata);
		
		short s = 5262;
		Short shorta = s;
		System.out.println(shorta);
		
		double d = 59551.5541;
		Double doublea = d;
		System.out.println(doublea);
		
		boolean p = true;
		Boolean booleana = p;
		System.out.println(booleana);
	}
}
