package assiment1;
/*Declare and initialize all 8 primitive datatypes, store them in their wrapper class and print those as a Object.
*/
public class wrapperclass {
		public static void main(String[] args) {
			
			int a = 12;
			Integer A = a;
        	System.out.println(A);
        	
        	char b = 's';
        	Character B = b;
        	System.out.println(B);
        	
        	byte c  = 112;
        	Byte C = c;
        	System.out.println(C);
        	
        	short d = 1255;
        	Short D = d;
        	System.out.println(D);
        	
        	long e = 125956295466L;
        	Long E = e;
        	System.out.println(E);
        	
        	double f = 912.265;
        	Double F = f;
        	System.out.println(F);
        	
        	float g = 96.55f;
        	Float G = g;
        	System.out.println(G);
        	
        	boolean h = true;
        	Boolean H = h;
        	System.out.println(H);	
        	
		}
}
